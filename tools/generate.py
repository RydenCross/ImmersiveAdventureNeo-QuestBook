(Q/'chapter_groups.snbt').write_text('{\n\tchapter_groups: [ ]\n}\n')
# Keep these empty until schema is generated by editor; quest rewards are embedded safely.
(Q/'rewards.snbt').write_text('{\n}\n')
(Q/'loot_crates.snbt').write_text('{\n}\n')

lang=['{',f'\tchapter.{chapter_id}.title: "Immersive Adventure Neo"']
for q in quests:
    lang.append(f'\tquest.{ids[q["code"]]}.title: "{q["title"]}"')
lang.append('}')
(Q/'lang/en_us.snbt').write_text('\n'.join(lang)+'\n')

source={'project':'Immersive Adventure Neo','format_version':13,'chapter':{'code':'WELCOME','id':chapter_id,'filename':'00_welcome'},'quests':quests,'generated_ids':ids}
(ROOT/'source/welcome.json').write_text(json.dumps(source,indent=2),encoding='utf-8')

