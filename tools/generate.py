from pathlib import Path
import hashlib, json, shutil, zipfile

ROOT=Path('/mnt/data/ImmersiveAdventureNeo-QuestBook')
if ROOT.exists(): shutil.rmtree(ROOT)
Q=ROOT/'config/ftbquests/quests'
(Q/'chapters').mkdir(parents=True)
(Q/'lang').mkdir(parents=True)
(ROOT/'tools').mkdir(parents=True)
(ROOT/'source').mkdir(parents=True)
(ROOT/'docs').mkdir(parents=True)

def hid(key):
    return hashlib.sha256(('ImmersiveAdventureNeo:'+key).encode()).hexdigest()[:16].upper()

chapter_id=hid('chapter:welcome')
quests=[
 dict(code='W01', title='Welcome to Immersive Adventure Neo', type='checkmark', x=0, y=0),
 dict(code='W02', title='Read the Journey Guide', type='checkmark', x=2, y=0, deps=['W01'], reward=('minecraft:bread',16)),
 dict(code='W03', title='Your First Log', type='item', item='minecraft:oak_log', count=1, x=4, y=0, deps=['W02'], reward=('minecraft:torch',16)),
 dict(code='W04', title='A Place to Work', type='item', item='minecraft:crafting_table', count=1, x=6, y=0, deps=['W03'], reward=('minecraft:stick',16)),
 dict(code='W05', title='The Stone Age', type='advancement', advancement='minecraft:story/mine_stone', x=8, y=0, deps=['W04'], reward=('minecraft:coal',8)),
 dict(code='W06', title='Acquire Hardware', type='advancement', advancement='minecraft:story/smelt_iron', x=10, y=0, deps=['W05'], reward=('minecraft:iron_ingot',4)),
 dict(code='W07', title='Sweet Dreams', type='advancement', advancement='minecraft:adventure/sleep_in_bed', x=12, y=0, deps=['W06'], reward=('minecraft:cooked_beef',8)),
 dict(code='W08', title='Ready for Adventure', type='checkmark', x=14, y=0, deps=['W07'], reward=('minecraft:torch',32)),
]
ids={q['code']:hid('quest:'+q['code']) for q in quests}

def task_block(q):
    tid=hid('task:'+q['code'])
    common='''\n\t\t\t\tReskillable: {\n\t\t\t\t\tcheck: 0b\n\t\t\t\t\tcomparison: "GREATER_OR_EQUAL"\n\t\t\t\t\tfirst: 10\n\t\t\t\t\tmode: "SKILL_LEVEL"\n\t\t\t\t\tsecond: 20\n\t\t\t\t\tskill_id: "mining"\n\t\t\t\t}\n\t\t\t\tSkillsLevel: {\n\t\t\t\t\tcategory: ""\n\t\t\t\t\tcheck: 0b\n\t\t\t\t\tcomparison: "GREATER_OR_EQUAL"\n\t\t\t\t\tfirst: 10\n\t\t\t\t\tmode: "TOTAL_LEVEL"\n\t\t\t\t\tsecond: 20\n\t\t\t\t}\n'''
    if q['type']=='checkmark': extra=f'\t\t\t\tid: "{tid}"\n\t\t\t\ttype: "checkmark"\n'
    elif q['type']=='item': extra=f'\t\t\t\tid: "{tid}"\n\t\t\t\titem: {{ count: {q.get("count",1)}, id: "{q["item"]}" }}\n\t\t\t\ttype: "item"\n'
    elif q['type']=='advancement': extra=f'\t\t\t\tadvancement: "{q["advancement"]}"\n\t\t\t\tcriterion: ""\n\t\t\t\tid: "{tid}"\n\t\t\t\ttype: "advancement"\n'
    return '\t\t\ttasks: [{' + common + extra + '\t\t\t}]\n'

def reward_block(q):
    if not q.get('reward'): return ''
    item,count=q['reward']; rid=hid('reward:'+q['code'])
    return f'''\t\t\trewards: [{{\n\t\t\t\tcount: {count}\n\t\t\t\tid: "{rid}"\n\t\t\t\titem: {{\n\t\t\t\t\tcount: 1\n\t\t\t\t\tid: "{item}"\n\t\t\t\t}}\n\t\t\t\ttype: "item"\n\t\t\t}}]\n'''

parts=['{\n\tdefault_hide_dependency_lines: false\n\tdefault_quest_shape: ""\n\tfilename: "00_welcome"\n\tgroup: ""\n',f'\tid: "{chapter_id}"\n\torder_index: 0\n\tquest_links: [ ]\n\tquests: [\n']
for i,q in enumerate(quests):
    parts.append('\t\t{\n')
    if q.get('deps'):
        deps=', '.join(f'"{ids[d]}"' for d in q['deps'])
        parts.append(f'\t\t\tdependencies: [{deps}]\n')
    parts.append(f'\t\t\tid: "{ids[q["code"]]}"\n')
    parts.append(reward_block(q))
    parts.append(task_block(q))
    parts.append(f'\t\t\tx: {float(q["x"]):.1f}d\n\t\t\ty: {float(q["y"]):.1f}d\n')
    parts.append('\t\t}' + ('' if i==len(quests)-1 else '') + '\n')
parts.append('\t]\n}\n')
(Q/'chapters/00_welcome.snbt').write_text(''.join(parts),encoding='utf-8')

# Exact baseline settings from uploaded v13 scaffold
